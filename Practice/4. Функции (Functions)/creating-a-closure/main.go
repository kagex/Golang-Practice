package main

import "fmt"

// Демонстрирует использование замыкания для накопления суммы.
func main() {
	// Вызываем adder(10), что возвращает функцию-замыкание.
	// Внутри adder параметр n инициализируется значением 10.
	// Это замыкание "запоминает" значение n между вызовами.
	add := adder(10) // Создаем замыкание с начальным значением 10

	// Вызываем замыкание с аргументом 5.
	// Внутри замыкания: n = 10 (из предыдущего вызова adder) + 5 = 15.
	// Значение 15 сохраняется в n для следующего вызова.
	fmt.Println(add(5)) // Ожидаемый результат: 15

	// Вызываем замыкание с аргументом 10.
	// Внутри замыкания: n = 15 (из предыдущего вызова) + 10 = 25.
	// Значение 25 сохраняется в n.
	fmt.Println(add(10)) // Ожидаемый результат: 25
}

// adder — функция-фабрика, создающая замыкания для накопления суммы.
// Принимает начальное значение n и возвращает функцию, которая будет
// добавлять к этой сумме переданные значения.
func adder(n int) func(x int) int {
	// Внутренняя анонимная функция использует параметр n (захвачен из внешней функции).
	// - принимает целое число x;
	// - добавляет его к значению n;
	// - возвращает обновлённое значение n.
	return func(x int) int {
		n += x
		return n
	}
}
