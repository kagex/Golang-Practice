package main

import (
	"errors"
	"fmt"
)

// Получает баллы от пользователя и выводит их количество и соответствующую оценку
func main() {
	// Вызываем функцию для получения баллов от пользователя
	// Возвращает пару (значение баллов, ошибка при вводе)
	points, err := getPoints()
	// Если произошла ошибка при вводе, выводим сообщение об ошибке и завершаем программу
	if err != nil {
		fmt.Printf("Возникла следующая ошибка: %v", err)
		return
	}

	// Выводим количество баллов и соответствующую оценку, полученную из функции getLetterGrade
	fmt.Printf("Количество ваших баллов: %d, ваша оценка: %s", points, getLetterGrade(points))
}

// Функция для получения баллов от пользователя
// Возвращает пару (количество баллов, ошибка при вводе)
func getPoints() (userPoints int, err error) {
	// Запрашиваем у пользователя ввод количества баллов
	fmt.Println("Введите количество ваших баллов:")
	// Считываем введённое значение в переменную userPoints
	// Первый возвращаемый значение от fmt.Scanln игнорируем (это количество считанных элементов)
	_, err = fmt.Scanln(&userPoints)

	// Если при считывании произошла ошибка (например, пользователь ввёл текст вместо числа),
	// возвращаем нулевое значение и ошибку с сообщением о некорректном вводе
	if err != nil {
		return 0, errors.New("введено некоректное значение, ожидалось целое число")
	}

	// Проверяем, что введённое значение находится в допустимом диапазоне от 0 до 100 включительно
	// Если значение выходит за границы диапазона, возвращаем ошибку с соответствующим сообщением
	if userPoints > 100 || userPoints < 0 {
		return 0, errors.New("выход за границы диапазона [0;100]")
	}

	// Если все проверки пройдены успешно, возвращаем введённое количество баллов и nil (без ошибки)
	return userPoints, nil
}

// Функция для определения буквенной оценки по количеству баллов
// Принимает количество баллов (целое число от 0 до 100)
// Возвращает буквенную оценку в виде строки ("A", "B", "C", "D" или "F")
func getLetterGrade(userPoints int) string {
	// Используем switch без условия для последовательной проверки диапазонов баллов
	switch {
	// Если баллов 90 или больше, возвращаем оценку "A"
	case userPoints >= 90:
		return "A"
	// Иначе, если баллов 80 или больше, возвращаем оценку "B"
	case userPoints >= 80:
		return "B"
	// Иначе, если баллов 70 или больше, возвращаем оценку "C"
	case userPoints >= 70:
		return "C"
	// Иначе, если баллов 60 или больше, возвращаем оценку "D"
	case userPoints >= 60:
		return "D"
	// Иначе (если баллов меньше 60), возвращаем оценку "F"
	default:
		return "F"
	}
}
