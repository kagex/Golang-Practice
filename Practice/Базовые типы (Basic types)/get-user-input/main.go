package main

import (
	"fmt"
	"strconv"
)

func main() {
	// Объявляем переменные для хранения имени, фамилии и возраста пользователя
	var userFirstName, userSecondName, userAge string

	// Запрашиваем у пользователя ввод имени и фамилии через пробел
	fmt.Println("Привет, скажи свое имя и фамилию (пожалуйста укажи через пробел)")

	// Выводим промпт для ввода имени и фамилии
	fmt.Print("\nМеня зовут: ")

	// Считываем два значения: имя и фамилию
	// Важно: при отладке через ввод может не работать корректно.
	// Рекомендуется запускать через "go run main.go" или использовать launch.json с "console": "integratedTerminal"
	fmt.Scanln(&userFirstName, &userSecondName)

	// Запрашиваем возраст пользователя и выводим приветствие с именем
	fmt.Printf("\nОтлично, %s.\nПодскажи пожалуйста сколько тебе лет.\nМне: ", userFirstName)

	// Считываем возраст в виде строки
	fmt.Scanln(&userAge)

	// Преобразуем строковый возраст в целое число
	// Второй возвращаемый параметр (ошибка) игнорируется с помощью _
	userParsedAge, _ := strconv.Atoi(userAge)

	// Выводим финальное сообщение с использованием всех введённых данных
	fmt.Printf("\nПриятно познакомиться, %s.\nЯ 5 лет назад познакомился с человеком, у которого тоже фамилия %s, вам тогда было %d.\nКак молоды мы были!", userFirstName, userSecondName, userParsedAge-5)
}
